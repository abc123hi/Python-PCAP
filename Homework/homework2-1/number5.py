# sample list
list_1 = [12,13,14,15,15,6,8,12]
# while loop iteration
i = 0
while i < len(list_1):
    print('iterated')
    i += 1
# for loop iteration
for i in range(i):
    print('iterated')
# for loop iteration with in
for i in list_1:
    print('iterated')
# for loop iteration with in and enumerate
for i in enumerate(list_1):
    print('iterated')
